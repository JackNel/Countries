public class Country {
    String abbr;
    String name;


    public Country(String abbr, String name) {
        this.abbr = abbr;
        this.name = name;

    }

    public String getAbbr() {
        return abbr;
    }//abbr getter

    public String getName() {
        return name;
    }//name getter
}//class Country
